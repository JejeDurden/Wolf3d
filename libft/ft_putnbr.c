#include "libft.h"

void	ft_putnbr(long long int n)
{
	if (n < -9223372036854775807)
		ft_putstr("9223372036854775808");
	if (n >= -9223372036854775807 && n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	else if (n <= 9223372036854775807 && n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else if (n <= 9 && n >= 0)
		ft_putchar(n + '0');
}
